#ข้อมูลที่ input มีบรรทัดแรกเป็นจำนวนเต็ม n ตามด้วยอีก n บรรทัดเป็นจำนวนเต็ม d0, d1, ..., dn-1 บรรทัดละจำนวน 
#จงเขียนโปรแกรมอ่านข้อมูลจาก input (โดยใช้วงวน while) เพื่อหาผลรวมของ d0, d1, ..., dn-1

#คำตอบ
n = int(input())
m = 0
p = 0
while m < n: #prove ว่า n เป็นจำนวนเต็ม จำนวนเต็มต้องมากกว่า 0
    p += int(input()) #คำนวณผลรวม
    m += 1 
print(p)
